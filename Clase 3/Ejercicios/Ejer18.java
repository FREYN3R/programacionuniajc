package Ejercicios;

public class Ejer18 {

        public static void main(String[] args) {
            // Generar y mostrar la tabla de multiplicar del 10
            int i = 10;
            for (int j = 1; j <= 10; j++) {
                System.out.printf("%d x %d = %d\n", i, j, i * j);
            }
        }
    }